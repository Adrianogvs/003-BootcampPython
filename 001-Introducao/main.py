# Primeiros passos em Python
# Instalação:
"""
Você pode baixar o Python em python.org. Após instalar, use o IDLE ou outro editor como VSCode para escrever seus códigos.
"""

# Seu primeiro programa:
print("Olá, Mundo!")  # Esse comando imprime o texto na tela.

# Conceitos Básicos:

# Variáveis:
# Usadas para armazenar dados.
nome = "Maria"
idade = 25
print("Nome:", nome)
print("Idade:", idade)

# Operações matemáticas:
# Você pode fazer cálculos facilmente.
a = 10
b = 5
soma = a + b
print("Soma:", soma)

# Estruturas condicionais:
# Para tomar decisões no código.
numero = 7
if numero > 0:
    print("O número é positivo.")
else:
    print("O número é negativo.")

# Laços de repetição:
# Para executar algo várias vezes.
for i in range(5):
    print("Repetição número", i)

# Funções:
# Para organizar e reutilizar seu código.
def saudacao(nome):
    print(f"Olá, {nome}!")

saudacao("João")

